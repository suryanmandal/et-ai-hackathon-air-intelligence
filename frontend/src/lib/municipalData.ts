// Helper to generate a high-definition organic multi-vertex municipal boundary polygon
function generateOrganicCityBoundary(center: [number, number], rx: number = 0.07, ry: number = 0.07, vertices: number = 24): [number, number][] {
  const [clng, clat] = center;
  const poly: [number, number][] = [];
  const radialFactors = [1.0, 1.15, 0.85, 1.25, 0.95, 1.10, 0.80, 1.30, 0.90, 1.05, 1.20, 0.85, 1.15, 0.95, 1.25, 0.75, 1.10, 0.90, 1.30, 0.85, 1.05, 1.15, 0.95, 1.0];

  for (let i = 0; i < vertices; i++) {
    const angle = (i / vertices) * 2 * Math.PI;
    const factor = radialFactors[i % radialFactors.length];
    const lng = clng + Math.cos(angle) * rx * factor;
    const lat = clat + Math.sin(angle) * ry * factor;
    poly.push([Number(lng.toFixed(5)), Number(lat.toFixed(5))]);
  }
  poly.push(poly[0]);
  return poly;
}



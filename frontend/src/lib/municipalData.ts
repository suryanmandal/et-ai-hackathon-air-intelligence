// Utility to get municipal corps list for a given state code
export function getMunicipalCorpsForState(stateCode: string): MunicipalCorporation[] {
  if (stateCode === "MH") {
    return MAHARASHTRA_MUNICIPAL_CORPS;
  }
  return OTHER_STATES_CORPS[stateCode] || [];
}

// Utility to get highest AQI municipal corporation for a state
export function getHighestAqiCorpForState(stateCode: string): MunicipalCorporation {
  const corps = getMunicipalCorpsForState(stateCode);
  if (corps.length === 0) return MAHARASHTRA_MUNICIPAL_CORPS[0];
  return [...corps].sort((a, b) => b.aqi - a.aqi)[0];
}
